    public void validateFields() {
        String foodName = txtFood.getText();
        String price = txtPrice.getText();
        if (!foodName.equals("") && !price.equals("")) {
            btAdd.setEnabled(true);
        } else {
            btAdd.setEnabled(false);
        }
    }

