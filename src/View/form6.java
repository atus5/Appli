    private void btAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAddActionPerformed
        // TODO add your handling code here:
        ff food = new ff();
        food.setCategory((String) cbCategory.getSelectedItem());
        food.setFood(txtFood.getText());
        food.setPrice(Float.parseFloat(txtPrice.getText()));
        foodList.add(food);
        model.addRow(new Object[]{
            i++, food.getCategory(), food.getFood(), food.getPrice()
        });
        if (new DAO.ConnectionSQL().addFoodToSQL(food)) {
            JOptionPane.showMessageDialog(rootPane, "Add Success.");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Cannot add food to database !");
        }

    }//GEN-LAST:event_btAddActionPerformed
