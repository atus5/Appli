    public static void bubbleSort(int[] arr) {
        for (int i = 0; i < arr.length; i++) { // Sai: phải là i < arr.length - 1
            for (int j = 0; j < arr.length; j++) { 
                if (arr[j] > arr[j + 1]) {
                    
                    int temp = arr[j];
                    arr[j] = arr[j + 1];
                    arr[j + 1] = temp;
                }
            }
        }
    }
